#==============================================================================
# ■ Picture_EvilStatue
#------------------------------------------------------------------------------
# NPCのポートレートを表示
#==============================================================================

class Picture_EvilStatue < WindowBase
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize
    super((512-(256+32))/2, 148, 256+32, 256+32)
    set_picture
    self.visible = true
    self.z = 10
  end
  #--------------------------------------------------------------------------
  # ● 画像の表示
  #--------------------------------------------------------------------------
  def set_picture
    bitmap = Cache.battler($data_monsters[ConstantTable::EVILSTATUE].name, 0)
    self.contents.clear
    self.contents.blt(0, 0, bitmap, bitmap.rect, 255)
  end
end
