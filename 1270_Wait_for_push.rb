#==============================================================================
# ■ Wait_for_push
#------------------------------------------------------------------------------
# ボタンがおされるまで待機
#==============================================================================

class Wait_for_push < WindowBase
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize
    super( 0, 0, 32+32, 32+32)
    self.visible = false
    self.opacity = 0
  end
  #--------------------------------------------------------------------------
  # ● フレーム更新
  #--------------------------------------------------------------------------
  def update
    super
    update_input_key
  end
  #--------------------------------------------------------------------------
  # ● テキスト設定
  #     text  : ウィンドウに表示する文字列
  #     align : アラインメント (0..左揃え、1..中央揃え、2..右揃え)
  #--------------------------------------------------------------------------
  def update_input_key
    if Input.trigger?(Input::C) and self.visible
      self.visible = false
    elsif Input.trigger?(Input::B) and self.visible
      self.visible = false
    end
  end
end
