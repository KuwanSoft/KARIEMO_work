#==============================================================================
# ■ WindowPubHelp
#------------------------------------------------------------------------------
# ショップ画面で、購入できる商品の一覧を表示するウィンドウです。
#==============================================================================

class WindowPubHelp < WindowBase
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #     x : ウィンドウの X 座標
  #     y : ウィンドウの Y 座標
  #--------------------------------------------------------------------------
  def initialize
    super((512-320)/2, WLH*6-6, 320, 24+32)
    self.visible = false
    self.z = 255
    change_font_to_v
    refresh
  end
  #--------------------------------------------------------------------------
  # ● リフレッシュ
  #--------------------------------------------------------------------------
  def refresh
    text = "だれをパーティにさそいますか?"
    self.contents.draw_text(0, 0, self.width-32, 24, text, 1)
  end
end
