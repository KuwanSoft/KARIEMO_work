class Items2
  attr_reader :id
  attr_reader :sort
  attr_reader :name
  attr_reader :name2
  attr_reader :kind
  attr_reader :rank
  attr_reader :icon
  attr_reader :purpose
  attr_reader :weight
  attr_reader :consumable
  attr_reader :use
  attr_reader :target
  attr_reader :target_num
  attr_reader :anim_id
  attr_reader :stack
  attr_reader :element
  attr_reader :add_state_set
  attr_reader :element_type
  attr_reader :remove_state_set
  attr_reader :apply
  attr_reader :special
  attr_reader :price
  attr_reader :token
  attr_reader :tool_shop
  attr_reader :key
  attr_reader :ex
  attr_reader :stock
  attr_reader :hide
  attr_reader :depth
  attr_reader :cp
  attr_reader :comment
  attr_reader :damage
  attr_reader :ing1_id
  attr_reader :ing2_id
  #--------------------------------------------------------------------------
  # ● 名前を取得
  #--------------------------------------------------------------------------
  def name
    return @name.delete("\"")
  end
  #--------------------------------------------------------------------------
  # ● 名前を取得
  #--------------------------------------------------------------------------
  def name2
    return @name2.delete("\"")
  end
  #--------------------------------------------------------------------------
  # ● アイコン名を取得
  #--------------------------------------------------------------------------
  def icon
    return @icon.to_s
  end
  #--------------------------------------------------------------------------
  # ● 付加異常を取得
  #--------------------------------------------------------------------------
  def add_state_set
    return @add_state_set.delete("\"")
  end
  #--------------------------------------------------------------------------
  # ● 回復異常を取得
  #--------------------------------------------------------------------------
  def remove_state_set
    return @remove_state_set.delete("\"")
  end
  #--------------------------------------------------------------------------
  # ● コメントを取得
  #--------------------------------------------------------------------------
  def comment
    return @comment.delete("\"")
  end
  #--------------------------------------------------------------------------
  # ● 対象は敵側か
  #--------------------------------------------------------------------------
  def for_opponent?
    return ["group","solo","all","random"].include?(@target)
  end
  #--------------------------------------------------------------------------
  # ● 対象はグループか
  #--------------------------------------------------------------------------
  def group?
    return @target == "group"
  end
  #--------------------------------------------------------------------------
  # ● 対象は単体か
  #--------------------------------------------------------------------------
  def solo?
    return ["solo","friend"].include?(@target)
  end
  #--------------------------------------------------------------------------
  # ● 対象は全体か
  #--------------------------------------------------------------------------
  def all?
    return @target == "all"
  end
  #--------------------------------------------------------------------------
  # ● 対象はランダムか
  #--------------------------------------------------------------------------
  def random?
    return @target == "random"
  end
  #--------------------------------------------------------------------------
  # ● 対象は見方側か
  #--------------------------------------------------------------------------
  def for_friend?
    return ["party","friend","self"].include?(@target)
  end
  #--------------------------------------------------------------------------
  # ● 対象の選択が必要か
  #--------------------------------------------------------------------------
  def need_selection?
    return ["group","solo","friend"].include?(@target)
  end
  #--------------------------------------------------------------------------
  # ● ターゲットが必要か（イベントや対象が必要無い場合はいらない）
  #--------------------------------------------------------------------------
  def need_target?
    return ["group","solo","party","all","random","self","friend"].include?(@target)
  end
  #--------------------------------------------------------------------------
  # ● 自分自身に使用するか？
  #--------------------------------------------------------------------------
  def for_user?
    return ["self"].include?(@target)
  end
  #--------------------------------------------------------------------------
  # ● 味方全員？
  #--------------------------------------------------------------------------
  def for_friends_all?
    return ["party"].include?(@target)
  end
  #--------------------------------------------------------------------------
  # ● パーティマジックか？
  #--------------------------------------------------------------------------
  def party_magic?
    return @use == "partymagic" # メニューからのみの使用（CAMP）
  end
  #--------------------------------------------------------------------------
  # ● 渡し橋か？
  #--------------------------------------------------------------------------
  def bridge?
    return @purpose == "bridge" # 特殊アイテム
  end
  #--------------------------------------------------------------------------
  # ● マップキットか？
  #--------------------------------------------------------------------------
  def mapkit?
    return false
  end
  #--------------------------------------------------------------------------
  # ● スキルブックか？
  #--------------------------------------------------------------------------
  def skillbook?
    return @kind == "skillbook"
  end
  #--------------------------------------------------------------------------
  # ● スキルブック2か？
  #--------------------------------------------------------------------------
  def skillbook2?
    return @kind == "skillbook2"
  end
  #--------------------------------------------------------------------------
  # ● 重量
  #--------------------------------------------------------------------------
  def weight
    return @weight.to_f
  end
  #--------------------------------------------------------------------------
  # ● スタック可能か？
  #--------------------------------------------------------------------------
  def stackable?
    return @stack > 0
  end
  #--------------------------------------------------------------------------
  # ● money
  #--------------------------------------------------------------------------
  def money?
    return false
  end
  #--------------------------------------------------------------------------
  # ● token
  #--------------------------------------------------------------------------
  def token?
    return (@kind == "token")
  end
  #--------------------------------------------------------------------------
  # ● food
  #--------------------------------------------------------------------------
  def food?
    return (@kind == "food")
  end
  #--------------------------------------------------------------------------
  # ● ガラクタか？
  #--------------------------------------------------------------------------
  def garbage?
    return (@kind == "garbage")
  end
  #--------------------------------------------------------------------------
  # ● Dummy主義
  #--------------------------------------------------------------------------
  def principle
    return ""
  end
  #--------------------------------------------------------------------------
  # ● ピックツールか？
  #--------------------------------------------------------------------------
  def picktool?
    return (@purpose == "picktool")
  end
  #--------------------------------------------------------------------------
  # ● 必要トークン（福引券）数
  #--------------------------------------------------------------------------
  def token
    return @token.to_i
  end
end
