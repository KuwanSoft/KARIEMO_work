class Data_Version
  #--------------------------------------------------------------------------
  # ● 公開インスタンス変数
  #--------------------------------------------------------------------------
  attr_accessor :ver
  attr_accessor :date
  attr_accessor :build
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize
    @ver = 0.01
    @date = 210811
    @build = 1
  end
  #--------------------------------------------------------------------------
  # ● バージョンストリングの作成
  #--------------------------------------------------------------------------
  def get_version_string
    return "#{@ver}"+"."+"#{@date}"+"-"+"#{@build}"
  end
  #--------------------------------------------------------------------------
  # ● バージョンの書き出し
  # Scene_Titleのcreate_versionで書き込まれる
  #--------------------------------------------------------------------------
  def store_version(new_version)
    @version_string = new_version
  end
  #--------------------------------------------------------------------------
  # ● バージョンの読み出し
  #--------------------------------------------------------------------------
  def read_version
    return @version_string
  end
  #--------------------------------------------------------------------------
  # ● ユニークIDの書き出し
  # Scene_Titleのcreate_versionで書き込まれる
  #--------------------------------------------------------------------------
  def store_uniqueid(uniqueid)
    @stored_uniqueid = uniqueid
  end
  #--------------------------------------------------------------------------
  # ● ユニークIDの読み出し
  #--------------------------------------------------------------------------
  def read_uniqueid
    return @stored_uniqueid
  end
  #--------------------------------------------------------------------------
  # ● ver
  #--------------------------------------------------------------------------
  def ver
    return @ver
  end
end
