#==============================================================================
# ■ Window_NPCDiscount
#------------------------------------------------------------------------------
# NPCの割引率を表示
#==============================================================================

class Window_NPCDiscount < WindowBase
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize(discount)
    super(512-(WLW*8+32), 70, WLW*8+32, 32*1+32)
    # self.z = self.z - 1
    refresh(discount)
  end
  #--------------------------------------------------------------------------
  # ● リフレッシュ
  #--------------------------------------------------------------------------
  def refresh(discount)
    @discount = discount
    self.contents.clear
    self.contents.draw_text(0, 0, self.width-32, WLH, "ねびき:")
    self.contents.draw_text(0, WLH, self.width-32, WLH, "-#{@discount}%", 2)
  end
end
