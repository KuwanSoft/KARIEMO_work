class Window_EQUIPBase < WindowBase
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #     x : ウィンドウの X 座標
  #     y : ウィンドウの Y 座標
  #--------------------------------------------------------------------------
  def initialize
    super((512-340)/2, 80, 340, WLH*13+32)
    self.z = 114
  end
  #--------------------------------------------------------------------------
  # ● 部位の表示
  #--------------------------------------------------------------------------
  def draw_title(text)
    self.contents.clear
    self.contents.draw_text(0, 0, self.width-32, BLH, text, 1) # 題名
  end
end
