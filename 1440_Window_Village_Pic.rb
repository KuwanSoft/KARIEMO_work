#==============================================================================
# ■ Window_Village_Pic
#------------------------------------------------------------------------------
# 村の画像を表示
#==============================================================================

class Window_Village_Pic < Window_Base
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #     x : ウィンドウの X 座標
  #     y : ウィンドウの Y 座標
  #--------------------------------------------------------------------------
  def initialize
    super(0, 0, 512, 448)
    self.opacity = 0
    @picture = Sprite.new
    draw_picture
  end
  #--------------------------------------------------------------------------
  # ● 村の表示
  #--------------------------------------------------------------------------
  def draw_picture
    @picture.bitmap = Cache.system("Hursley3.png")
    @picture.ox = @picture.bitmap.width / 2
    @picture.oy = @picture.bitmap.height / 2
    @picture.x = 512 / 2
    @picture.y = 448 / 2
  end
  #--------------------------------------------------------------------------
  # ● 解放
  #--------------------------------------------------------------------------
  def dispose
    @picture.bitmap.dispose
    @picture.dispose
  end
end
