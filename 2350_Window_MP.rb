class Window_MP < WindowBase
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #     x : ウィンドウの X 座標
  #     y : ウィンドウの Y 座標
  #--------------------------------------------------------------------------
  def initialize
    super( (512-(WLW*15+32))/2, WLH*19, WLW*15+32, 32+32)
    self.visible = false
    self.z = 255
  end
  #--------------------------------------------------------------------------
  # ● 更新
  #--------------------------------------------------------------------------
  def refresh(actor = nil)
    self.contents.clear
    @actor = actor unless actor == nil
    draw_mplist( 0, 0, @actor)
  end
end
