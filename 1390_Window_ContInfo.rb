#==============================================================================
# ■ Window_ContInfo
#------------------------------------------------------------------------------
# 中断データの再開時に表示するウインドウ
#==============================================================================

class Window_ContInfo < Window_Base
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize
    super(0, 200, 512, 24*2+32)
    change_font_to_v
    self.visible = true
    self.z = 255
    draw_text
  end
  #--------------------------------------------------------------------------
  # ● テキストの描画
  #--------------------------------------------------------------------------
  def draw_text
    self.contents.clear
    text1 = "ちゅうだんデータがそんざいします。"
    text2 = "[A]でさいかい"
    self.contents.draw_text(0, 24*0, self.width-32, 24, text1)
    self.contents.draw_text(0, 24*1, self.width-32, 24, text2, 2)
  end
  #--------------------------------------------------------------------------
  # ● フレーム更新
  #--------------------------------------------------------------------------
  def update
    super
    update_input_key  # 入力受付
  end
  #--------------------------------------------------------------------------
  # ● 入力確認後消去
  #--------------------------------------------------------------------------
  def update_input_key
    if Input.trigger?(Input::C) and self.visible
      self.visible = false
    end
  end
end
