class Window_ContinueHelp < WindowBase
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize
    super(0, 0, 512, 24 + 32)
    self.visible = false
    self.z = 103
    change_font_to_v
    refresh
  end
  #--------------------------------------------------------------------------
  # ● リフレッシュ
  #--------------------------------------------------------------------------
  def refresh
    self.contents.clear
    text = "さいかいするパーティをえらんでください。"
    self.contents.draw_text(0, 0, 512-32, 24, text, 1)
  end
end
